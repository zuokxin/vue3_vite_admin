<template>
  <div>
    <!-- svg:图标外层容器节点，内部需要与use标签结合使用 -->
    <svg :style="{ width, height }">
      <!-- xlink:href执行用哪个图标，属性值需要使用#icon-图标名字 -->
      <!-- use标签fill属性设置图标颜色 -->
      <use :xlink:href="prefix + name" :fill="color"></use>
    </svg>
  </div>
</template>

<script setup lang="ts">
// 接受父组件传递过来的参数
defineProps({
  // xlink:href属性值前缀
  prefix: {
    type: String,
    default: '#icon-',
  },
  // 提供使用的图标名字
  name: String,
  // 接受父组件传递颜色
  color: {
    type: String,
    default: '',
  },
  // 接受父组件传递过来的图标的宽度
  width: {
    type: String,
    default: '16px',
  },
  // 接受父组件传递过来的图标的高度
  height: {
    type: String,
    default: '16px',
  },
})
</script>

<style lang="scss" scoped></style>
